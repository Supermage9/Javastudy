package study;

public class OperExam07 {

	public static void main(String[] args) {
		
		
		// 복합대입연산자
		int a = 10;
		int result = 0;
		
		result += a;
		System.out.println("result ="+result);
		result -= a;
		System.out.println("result ="+result);
		result *= a;
		System.out.println("result ="+result);
		result /= a;
		System.out.println("result ="+result);
		result %= a;
		System.out.println("result ="+result);
		
		
		
		
		
	}

}
